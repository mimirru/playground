weather = 'snowy'

case weather
	when weather = 'cloudy'
		puts "Cloudy, but warm! Light jacket is all you'll need!"

	when weather = 'foggy'
		puts "Wear bright clothing!"

	when weather = 'rainy'
		puts "Galoshes, rain coat, umbrella, oh my!"

	when weather = 'sunny'
		puts "Break out the shorts and t-shirts and lather on the sunscreen!"

	else
		puts "Wear anything you want!"
end